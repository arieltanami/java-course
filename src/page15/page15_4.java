package page15;

public class page15_4 {
     // (4) כתוב תכנית שמדפיסה ספירה לאחור: 10, 9, 8 , . . . , 1, 0
     // (ראה הערה למטה: במקום i++ יש לכתוב i-- )
     public static void main(String[] args) {
          for (int i = 10; i >= 0; i--) {
               System.out.println(i);
          }
     }
}
